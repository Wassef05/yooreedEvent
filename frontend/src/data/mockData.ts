// Helper: Get products by category slug
export function getMockProductsByCategory(slug: string): Product[] {
  if (!slug) return MOCK_PRODUCTS;
  return MOCK_PRODUCTS.filter(p => p.categorie === slug || p.sousCategorie === slug);
}

// Helper: Search mock products
export function searchMockProducts(query: string): Product[] {
  const q = query.toLowerCase();
  return MOCK_PRODUCTS.filter(p =>
    p.nom.toLowerCase().includes(q) ||
    p.description.toLowerCase().includes(q) ||
    p.categorie.toLowerCase().includes(q)
  );
}

// Helper: Get main categories only
export function getMainCategories(): Category[] {
  return MOCK_CATEGORIES.filter(c => c.parentId === null);
}

// Helper: Get subcategories by parent slug
export function getSubCategories(parentSlug: string): Category[] {
  const parent = MOCK_CATEGORIES.find(c => c.slug === parentSlug);
  if (!parent) return [];
  return MOCK_CATEGORIES.filter(c => c.parentId === parent._id);
}
